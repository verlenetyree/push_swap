#include "libft.h"

int	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (s == NULL)
		return (-1);
	while (*s)
	{
		i += ft_putchar_fd(*s++, fd);
	}
	return (i);
}
