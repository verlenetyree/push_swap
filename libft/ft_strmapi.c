#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	int				len;
	char			*out;

	i = 0;
	if (s == NULL)
		return (NULL);
	len = ft_strlen(s);
	out = (char *)malloc(sizeof(char) * len + 1);
	if (out == NULL)
		return (NULL);
	while (s[i])
	{
		out[i] = f(i, s[i]);
		i++;
	}
	out[i] = '\0';
	return (out);
}
